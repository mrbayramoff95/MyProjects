package lessons2;

public class switchCaseNumber {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            switch (i) {
                case 1:
                    System.out.println("один");
                    break;
                case 2:
                    System.out.println("два");
                    break;
                case 3:
                    System.out.println("три");
                    break;
                case 4:
                    System.out.println("четыре");
                    break;
                case 5:
                    System.out.println("пять");
                    break;
            }
        }
    }
}

